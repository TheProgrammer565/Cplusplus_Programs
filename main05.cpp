//Find the size of datatypes

#include <iostream>
using namespace std;

int main(){
	cout<<"Size of char datatype: "<<sizeof(char)<<"byte\n";
	cout<<"Size of int datatype: "<<sizeof(int)<<"bytes\n";
	cout<<"Size of float datatype: "<<sizeof(float)<<"bytes\n";
	cout<<"Size of double datatype: "<<sizeof(double)<<"bytes\n";
	return 0;
}
