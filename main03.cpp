//Sum of two numbers

#include<iostream>
using namespace std;

int main(){
	int firstNum,secondNum,sum,product;
	cout<<"Enter the value of First Number: ";
	cin>>firstNum;
	cout<<"Enter the value of Second Number: ";
	cin>>secondNum;
	sum=firstNum+secondNum;
	cout<<"The sum of two numbers is: "<<sum<<"\n";
	product=firstNum*secondNum;
	cout<<"The product of two numbers is: "<<product;
	return 0;
}
